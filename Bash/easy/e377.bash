#!/bin/bash

fit1() {
    echo "$((($1 / $3) * ($2 / $4)))"
}

fit2() {
    let a=$(fit1 "$@")
    let b=$(fit1 "$1" "$2" "$4" "$3")

    echo $((a > b ? a : b))
}

fit3() {
    vals=(
        "$((($1 / $4) * ($2 / $5) * ($3 / $6)))"
        "$((($1 / $4) * ($2 / $6) * ($3 / $5)))"
        "$((($1 / $5) * ($2 / $4) * ($3 / $6)))"
        "$((($1 / $5) * ($2 / $6) * ($3 / $4)))"
        "$((($1 / $6) * ($2 / $4) * ($3 / $5)))"
        "$((($1 / $6) * ($2 / $5) * ($3 / $4)))"
    )

    max=0
    for val in "${vals[@]}"; do
        max=$((val > max ? val : max))
    done
    echo $max
}
